import React, { useState, useEffect } from 'react';
import {
    Card, Form, Input, Button, Table, Modal, Select, InputNumber,
    Space, Tag, Typography, message, Row, Col, Statistic, Progress,
    DatePicker, Alert, Divider, Badge, Avatar, Tooltip
} from 'antd';
import {
    UserOutlined, GiftOutlined, CrownOutlined, StarOutlined,
    TrophyOutlined, PlusOutlined, EditOutlined, DeleteOutlined
} from '@ant-design/icons';
import { addCustomer, getCustomers, updateCustomer, addLoyaltyTransaction } from '../firebase/customer_service';
import dayjs from 'dayjs';

const { Title, Text } = Typography;
const { Option } = Select;

const CustomerLoyalty = () => {
    const [customers, setCustomers] = useState([]);
    const [loading, setLoading] = useState(false);
    const [modalVisible, setModalVisible] = useState(false);
    const [editingCustomer, setEditingCustomer] = useState(null);
    const [form] = Form.useForm();

    // C·∫•p ƒë·ªô th√†nh vi√™n
    const membershipTiers = [
        { level: 'Bronze', minPoints: 0, discount: 5, color: '#CD7F32', icon: 'ü•â' },
        { level: 'Silver', minPoints: 500, discount: 10, color: '#C0C0C0', icon: 'ü•à' },
        { level: 'Gold', minPoints: 1000, discount: 15, color: '#FFD700', icon: 'ü•á' },
        { level: 'Platinum', minPoints: 2000, discount: 20, color: '#E5E4E2', icon: 'üíé' }
    ];

    useEffect(() => {
        fetchCustomers();
    }, []);

    const fetchCustomers = async () => {
        try {
            setLoading(true);
            const customerList = await getCustomers();
            setCustomers(customerList);
        } catch (error) {
            message.error('Kh√¥ng th·ªÉ t·∫£i danh s√°ch kh√°ch h√†ng');
        } finally {
            setLoading(false);
        }
    };

    const getMembershipTier = (points) => {
        for (let i = membershipTiers.length - 1; i >= 0; i--) {
            if (points >= membershipTiers[i].minPoints) {
                return membershipTiers[i];
            }
        }
        return membershipTiers[0];
    };

    const handleSubmit = async (values) => {
        try {
            setLoading(true);
            const customerData = {
                ...values,
                loyaltyPoints: values.loyaltyPoints || 0,
                totalSpent: values.totalSpent || 0,
                visitCount: values.visitCount || 0,
                lastVisit: new Date(),
                createdAt: new Date()
            };

            if (editingCustomer) {
                await updateCustomer(editingCustomer.id, customerData);
                message.success('C·∫≠p nh·∫≠t kh√°ch h√†ng th√†nh c√¥ng!');
            } else {
                await addCustomer(customerData);
                message.success('Th√™m kh√°ch h√†ng th√†nh c√¥ng!');
            }

            setModalVisible(false);
            form.resetFields();
            setEditingCustomer(null);
            fetchCustomers();
        } catch (error) {
            message.error('C√≥ l·ªói x·∫£y ra!');
        } finally {
            setLoading(false);
        }
    };

    const addPoints = async (customerId, points, reason) => {
        try {
            const customer = customers.find(c => c.id === customerId);
            const newPoints = (customer.loyaltyPoints || 0) + points;

            await updateCustomer(customerId, {
                loyaltyPoints: newPoints,
                lastVisit: new Date()
            });

            await addLoyaltyTransaction({
                customerId,
                points,
                type: 'earn',
                reason,
                timestamp: new Date()
            });

            message.success(`ƒê√£ c·ªông ${points} ƒëi·ªÉm cho kh√°ch h√†ng!`);
            fetchCustomers();
        } catch (error) {
            message.error('C√≥ l·ªói khi c·ªông ƒëi·ªÉm!');
        }
    };

    const columns = [
        {
            title: 'Kh√°ch h√†ng',
            key: 'customer',
            render: (_, record) => (
                <Space>
                    <Avatar icon={<UserOutlined />} />
                    <div>
                        <div style={{ fontWeight: 600 }}>{record.name}</div>
                        <div style={{ fontSize: 12, color: '#666' }}>{record.phone}</div>
                    </div>
                </Space>
            )
        },
        {
            title: 'H·∫°ng th√†nh vi√™n',
            key: 'tier',
            render: (_, record) => {
                const tier = getMembershipTier(record.loyaltyPoints || 0);
                return (
                    <Tag color={tier.color} style={{ color: '#fff', fontWeight: 600 }}>
                        {tier.icon} {tier.level}
                    </Tag>
                );
            }
        },
        {
            title: 'ƒêi·ªÉm t√≠ch l≈©y',
            dataIndex: 'loyaltyPoints',
            key: 'points',
            render: (points) => (
                <Badge count={points || 0} style={{ backgroundColor: '#52c41a' }} />
            )
        },
        {
            title: 'T·ªïng chi ti√™u',
            dataIndex: 'totalSpent',
            key: 'spent',
            render: (amount) => `${(amount || 0).toLocaleString()}ƒë`
        },
        {
            title: 'S·ªë l·∫ßn gh√© thƒÉm',
            dataIndex: 'visitCount',
            key: 'visits'
        },
        {
            title: 'L·∫ßn cu·ªëi gh√© thƒÉm',
            dataIndex: 'lastVisit',
            key: 'lastVisit',
            render: (date) => date ? dayjs(date.toDate()).format('DD/MM/YYYY') : 'Ch∆∞a c√≥'
        },
        {
            title: 'Thao t√°c',
            key: 'actions',
            render: (_, record) => (
                <Space>
                    <Button
                        type="primary"
                        size="small"
                        onClick={() => addPoints(record.id, 50, 'Th∆∞·ªüng th·ªß c√¥ng')}
                    >
                        +50 ƒëi·ªÉm
                    </Button>
                    <Button
                        size="small"
                        onClick={() => {
                            setEditingCustomer(record);
                            form.setFieldsValue(record);
                            setModalVisible(true);
                        }}
                    >
                        S·ª≠a
                    </Button>
                </Space>
            )
        }
    ];

    return (
        <div style={{ padding: '24px' }}>
            <Title level={2}>
                <CrownOutlined /> Ch∆∞∆°ng tr√¨nh kh√°ch h√†ng th√¢n thi·∫øt
            </Title>

            {/* Th·ªëng k√™ t·ªïng quan */}
            <Row gutter={16} style={{ marginBottom: 24 }}>
                <Col span={6}>
                    <Card>
                        <Statistic
                            title="T·ªïng kh√°ch h√†ng"
                            value={customers.length}
                            prefix={<UserOutlined />}
                        />
                    </Card>
                </Col>
                <Col span={6}>
                    <Card>
                        <Statistic
                            title="Kh√°ch VIP (Gold+)"
                            value={customers.filter(c => (c.loyaltyPoints || 0) >= 1000).length}
                            prefix={<CrownOutlined />}
                        />
                    </Card>
                </Col>
                <Col span={6}>
                    <Card>
                        <Statistic
                            title="T·ªïng ƒëi·ªÉm ƒë√£ ph√°t"
                            value={customers.reduce((sum, c) => sum + (c.loyaltyPoints || 0), 0)}
                            prefix={<GiftOutlined />}
                        />
                    </Card>
                </Col>
                <Col span={6}>
                    <Card>
                        <Statistic
                            title="T·ªïng doanh thu t·ª´ VIP"
                            value={customers.filter(c => (c.loyaltyPoints || 0) >= 1000)
                                .reduce((sum, c) => sum + (c.totalSpent || 0), 0)}
                            suffix="ƒë"
                            prefix={<TrophyOutlined />}
                        />
                    </Card>
                </Col>
            </Row>

            {/* B·∫£ng h·∫°ng th√†nh vi√™n */}
            <Card title="C√°c h·∫°ng th√†nh vi√™n" style={{ marginBottom: 24 }}>
                <Row gutter={16}>
                    {membershipTiers.map(tier => (
                        <Col span={6} key={tier.level}>
                            <Card size="small" style={{ textAlign: 'center' }}>
                                <div style={{ fontSize: 24, marginBottom: 8 }}>{tier.icon}</div>
                                <Title level={5}>{tier.level}</Title>
                                <Text>T·ª´ {tier.minPoints} ƒëi·ªÉm</Text><br />
                                <Text strong style={{ color: '#52c41a' }}>
                                    Gi·∫£m {tier.discount}%
                                </Text>
                            </Card>
                        </Col>
                    ))}
                </Row>
            </Card>

            {/* Danh s√°ch kh√°ch h√†ng */}
            <Card
                title="Danh s√°ch kh√°ch h√†ng th√¢n thi·∫øt"
                extra={
                    <Button
                        type="primary"
                        icon={<PlusOutlined />}
                        onClick={() => setModalVisible(true)}
                    >
                        Th√™m kh√°ch h√†ng
                    </Button>
                }
            >
                <Table
                    columns={columns}
                    dataSource={customers}
                    rowKey="id"
                    loading={loading}
                    pagination={{ pageSize: 10 }}
                />
            </Card>

            {/* Modal th√™m/s·ª≠a kh√°ch h√†ng */}
            <Modal
                title={editingCustomer ? 'S·ª≠a th√¥ng tin kh√°ch h√†ng' : 'Th√™m kh√°ch h√†ng m·ªõi'}
                open={modalVisible}
                onCancel={() => {
                    setModalVisible(false);
                    form.resetFields();
                    setEditingCustomer(null);
                }}
                footer={null}
            >
                <Form form={form} onFinish={handleSubmit} layout="vertical">
                    <Form.Item
                        name="name"
                        label="T√™n kh√°ch h√†ng"
                        rules={[{ required: true, message: 'Vui l√≤ng nh·∫≠p t√™n!' }]}
                    >
                        <Input />
                    </Form.Item>
                    <Form.Item
                        name="phone"
                        label="S·ªë ƒëi·ªán tho·∫°i"
                        rules={[{ required: true, message: 'Vui l√≤ng nh·∫≠p s·ªë ƒëi·ªán tho·∫°i!' }]}
                    >
                        <Input />
                    </Form.Item>
                    <Form.Item name="email" label="Email">
                        <Input type="email" />
                    </Form.Item>
                    <Form.Item name="birthday" label="Ng√†y sinh">
                        <DatePicker style={{ width: '100%' }} />
                    </Form.Item>
                    <Form.Item name="loyaltyPoints" label="ƒêi·ªÉm t√≠ch l≈©y">
                        <InputNumber min={0} style={{ width: '100%' }} />
                    </Form.Item>
                    <Form.Item name="totalSpent" label="T·ªïng chi ti√™u">
                        <InputNumber min={0} style={{ width: '100%' }} />
                    </Form.Item>
                    <Form.Item>
                        <Space>
                            <Button type="primary" htmlType="submit" loading={loading}>
                                {editingCustomer ? 'C·∫≠p nh·∫≠t' : 'Th√™m m·ªõi'}
                            </Button>
                            <Button onClick={() => setModalVisible(false)}>
                                H·ªßy
                            </Button>
                        </Space>
                    </Form.Item>
                </Form>
            </Modal>
        </div>
    );
};

export default CustomerLoyalty;
